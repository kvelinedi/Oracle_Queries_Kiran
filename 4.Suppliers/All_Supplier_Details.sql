SELECT 
    S.SUPPLIER_HCC_ID, 
    S.SUPPLIER_NAME, 
    SL.SUPPLIER_LOCATION_HCC_ID, 
    SL.SUPPLIER_LOCATION_NAME, 
    CASE 
        WHEN SL.SUPPLIER_LOCATION_STATUS = 'A' THEN 'Active'
        WHEN SL.SUPPLIER_LOCATION_STATUS = 'T' THEN 'Terminated'
        ELSE SL.SUPPLIER_LOCATION_STATUS
    END AS SUPPLIER_LOCATION_STATUS,
    D.DATE_VALUE AS Supplier_location_effective_date, 
    DD.DATE_VALUE AS Supplier_location_term_date, 
    PA.ADDRESS_LINE, 
    PA.ADDRESS_LINE_2, 
    PA.ADDRESS_LINE_3, 
    PA.CITY_NAME, 
    PA.STATE_CODE, 
    PA.ZIP_CODE
FROM 
    SUPPLIER S
LEFT JOIN 
    SUPPLIER_LOCATION SL ON S.SUPPLIER_KEY = SL.SUPPLIER_KEY
LEFT JOIN 
    POSTAL_ADDRESS PA ON SL.PRACTICE_ADDRESS_KEY = PA.POSTAL_ADDRESS_KEY
LEFT JOIN 
    DATE_DIMENSION D ON SL.SUPPLIER_LOC_EFF_DATE_KEY = D.DATE_KEY
LEFT JOIN 
    DATE_DIMENSION DD ON SL.SUPPLIER_LOC_TERM_DATE_KEY = DD.DATE_KEY
WHERE 
    S.SUPPLIER_HCC_ID IN (
        '9003728', 'S0000115', '1013602', '9900411', '9900414', 
        '9900420', '9900413', '9900415', '9900492', '9903548', 
        '9001170', '9900410', '9900432', '1004839', '9900409', 
        '32471', '32825', '34652', '36002', '1000020', 
        '1000021', '1000187', '1000193', '1000200', '1000201', 
        '1000202', '1000203', '1000204', '1000205', '1000206', 
        '1001169', '1001170', '1001531', '1002039', '1002041', 
        '1002047', '1002053', '1012339', '1012341', '1098232', 
        'S0000699'
    )
ORDER BY 
    S.SUPPLIER_HCC_ID;